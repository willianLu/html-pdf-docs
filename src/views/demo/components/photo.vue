})

const img800_360 = ref('')
const img120_160 = ref('')
const img120_140 = ref('')
const img120_68 = ref('')

initImgData()
function initImgData() {
  Loading.show()
  const promiseArr = [
    networkImageToLocal('https://picsum.photos/800/360'),
    networkImageToLocal('https://picsum.photos/120/160'),
    networkImageToLocal('https://picsum.photos/120/140'),
    networkImageToLocal('https://picsum.photos/120/68'),
  ]
